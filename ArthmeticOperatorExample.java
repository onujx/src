public class ArthmeticOperatorExample {
    public static void main(String[] args) {
        int v1 = 5;
        int v2 = 2;

        int result1 = v1 + v2;
        System.out.println("result = " + result1);
        // Suggest. Double type

        double v3 = v2;

        double result2 = v1 / v3;

        System.out.println("result = " + result2);
        //c와 같이 1.0을 곱하거나 변수 중 하나를 double 타입으로 캐스팅 한 뒤 산술 연산을 하면 된다.
    }
}
